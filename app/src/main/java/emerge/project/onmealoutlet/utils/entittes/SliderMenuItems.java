public class SliderMenuItems {


    String mTitle;
    int mIcon;



    public SliderMenuItems(String title, int icon) {
        mTitle = title;
        mIcon = icon;
    }



    public String getmTitle() {
        return mTitle;
    }

    public void setmTitle(String mTitle) {
        this.mTitle = mTitle;
    }

    public int getmIcon() {
        return mIcon;
    }

    public void setmIcon(int mIcon) {
        this.mIcon = mIcon;
    }


}
